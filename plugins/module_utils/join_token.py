from typing import Optional


def extract_join_token(token_stdout: str) -> Optional[str]:
    # format -> Token: <token_string>
    if not token_stdout:
        return None
    splits = token_stdout.split(" ")
    if len(splits) < 2:
        return None
    return splits[-1].strip()
